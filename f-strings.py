name = "Amir"
print("Hello " + name + " nice to meet you!")  # + requires space
print("Hello", name, "nice to meet you!") # comma , does an auto space
print(f"Hello {name} nice to meet you!") # f string
print(f"Hello {name} nice to meet you! The result of 7x7 is {7*7}") # f string
print(f"I am breaking this down: {'this is my text'.split()}")
print(f"This is an example: {print('hello amir')}") # print returns none

